package com.exercise;

public class Ejercicio1 {
    public static void main(String[] args) {

        // Enteros
        byte numero1 = 4;
        short numero2 = 15;
        int numero3 = 80;
        long numero4 = 1700;

        System.out.println("Numeros enteros: " + numero1 + ", " + numero2 + ", " + numero3 + ", " + numero4);

        // Decimales
        float numero5 = 4.5f;
        double numero6 = 15.55d;

        System.out.println("Numeros decimales: " + numero5 + ", " + numero6);

        // Cantidad de caracteres
        char numero7 = 'a';
        char numero8 = 'A';

        System.out.println("Cantidad de caracteres: " + numero7 + ", " + numero8);

        // Cadena de texto
        String nombre = "Daniel";
        String apellido = "Ruiz Ledesma";

        System.out.println("Nombre: " + nombre + ", " + apellido);

        // Booleanos
        boolean verdadero = true;
        boolean falso = false;

        System.out.println("Estado: " + verdadero + ", " + falso);
    }
}